#include <unistd.h>

void	ft_putchar(char c)
{
	write(1, &c, 1);
}

void	ft_print_numbers(void)
{
	char	say;

	say = '0';
	while (say <= '9')
	{
		ft_putchar(say);
		say++;
	}
}
