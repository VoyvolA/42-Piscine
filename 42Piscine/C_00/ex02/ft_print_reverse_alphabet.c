#include <unistd.h>

void	ft_putchar(char c)
{
	write(1, &c, 1);
}

void	ft_print_reverse_alphabet(void)
{
	char	say;

	say = 'z';
	while (say >= 'a')
	{
		ft_putchar(say);
	say--;
	}
}
